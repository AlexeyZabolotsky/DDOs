"""Демонстрация без GUI: все функции вызываются сообщениями.

Запуск: python -m redstone.demo
"""

import json

from redstone.core import World, СОСТОЯНИЯ, сообщение

С = СОСТОЯНИЯ


def показать(w: World, заголовок: str) -> None:
    print(f"\n=== {заголовок} ===")
    for c in sorted(w.блоки):
        b = w.блоки[c]
        print(f"  {c} цвет={b.цвет} блок={b.заблокирован} выдел={b.выделен} "
              f"соед={sorted(b.соединения)} "
              f"память={json.dumps(b.память, ensure_ascii=False)} "
              f"сигнал={json.dumps(b.сигнал, ensure_ascii=False)}")


def main() -> None:
    w = World()

    # 1. Генерация цепочки блоков.
    for x in range(4):
        w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (x, 0, 0)))
    показать(w, "После генерации")

    # 2. Соединение в цепь.
    for x in range(3):
        w.dispatch(сообщение(С["СОЕДИНЕНИЕ"], (x, 0, 0), {"с": [x + 1, 0, 0]}))

    # 3. «Программа» внутри блока: при сигнале генерирует новый блок
    #    и красит себя — функции переданы через данные блока.
    w.dispatch(сообщение(С["СОХРАНЕНИЕ"], (3, 0, 0), {
        "программа": [
            {"состояние": С["ГЕНЕРАЦИЯ"], "координата": [3, 1, 0],
             "данные": {"цвет": "#4caf50", "память": {"создан": {"кем": "программа"}}}},
            {"состояние": С["ВЫДЕЛЕНИЕ"], "данные": {"цвет": "#e91e63"}},
        ],
    }))

    # 4. Передача вложенных данных по цепи.
    w.dispatch(сообщение(С["ПЕРЕДАЧА"], (0, 0, 0), {
        "сигнал": {"уровень": 15, "метаданные": {"источник": {"тип": "рычаг"}}},
    }))
    показать(w, "После передачи (сработала программа блока (3,0,0))")

    # 5. Блокировка передачи и повторный сигнал.
    w.dispatch(сообщение(С["БЛОКИРОВКА"], (2, 0, 0)))
    w.dispatch(сообщение(С["ПЕРЕДАЧА"], (0, 0, 0), {"сигнал": {"уровень": 3}}))
    показать(w, "Сигнал 3 остановлен заблокированным блоком (2,0,0)")

    # 6. Выделение области цветом и перемещение связной группы с данными.
    w.dispatch(сообщение(С["ВЫДЕЛЕНИЕ"], (0, 0, 0),
                         {"область": [[0, 0, 0], [1, 0, 0]], "цвет": "#ffd54f"}))
    w.dispatch(сообщение(С["РАЗЪЕДИНЕНИЕ"], (1, 0, 0), {"с": [2, 0, 0]}))
    w.dispatch(сообщение(С["ПЕРЕМЕЩЕНИЕ"], (0, 0, 0), {"сдвиг": [0, 3, 0]}))
    показать(w, "Группа {(0,0,0),(1,0,0)} переехала на (0,3,0)–(1,3,0)")

    # 7. Стирание и уничтожение.
    w.dispatch(сообщение(С["СТИРАНИЕ"], (3, 0, 0), {"ключи": ["программа"]}))
    w.dispatch(сообщение(С["УНИЧТОЖЕНИЕ"], (3, 1, 0)))
    показать(w, "После стирания программы и уничтожения блока (3,1,0)")

    print(f"\nВсего обработано сообщений: {len(w.журнал)}")


if __name__ == "__main__":
    main()
