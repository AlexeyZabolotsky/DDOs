"""Ядро редстоун-мира.

Мир (World) — это разреженная 3D-сетка блоков. Любое воздействие на мир
описывается сообщением (Message) в формате «ключ: значение»:

    {
        "состояние": "передача",            # команда / новое состояние
        "координата": (x, y, z),            # точка воздействия
        "данные": {"сигнал": {"уровень": 15, "метка": {"вглубь": ...}}},
    }

Поле "данные" допускает любую степень вложенности словаря.

Одни и те же сообщения порождаются:
  * мышью (UI собирает сообщение и вызывает ``World.dispatch``);
  * данными внутри блоков: если в памяти блока лежит список сообщений
    под ключом "программа", они исполняются, когда до блока доходит
    сигнал передачи.
"""

from __future__ import annotations

import copy
import json
from collections import deque
from dataclasses import dataclass, field
from typing import Any, Callable, Dict, Iterable, List, Optional, Set, Tuple

Coord = Tuple[int, int, int]
Message = Dict[str, Any]

# ---------------------------------------------------------------------------
# Словарь состояний (команд). Значения используются в поле "состояние".
# ---------------------------------------------------------------------------
СОСТОЯНИЯ = {
    "ГЕНЕРАЦИЯ": "генерация",
    "УНИЧТОЖЕНИЕ": "уничтожение",
    "СОХРАНЕНИЕ": "сохранение",
    "СТИРАНИЕ": "стирание",
    "ПЕРЕДАЧА": "передача",
    "БЛОКИРОВКА": "блокировка",
    "РАЗБЛОКИРОВКА": "разблокировка",
    "ВЫДЕЛЕНИЕ": "выделение",
    "СНЯТИЕ_ВЫДЕЛЕНИЯ": "снятие_выделения",
    "СОЕДИНЕНИЕ": "соединение",
    "РАЗЪЕДИНЕНИЕ": "разъединение",
    "ПЕРЕМЕЩЕНИЕ": "перемещение",
}


def сообщение(состояние: str, координата: Coord, данные: Optional[dict] = None) -> Message:
    """Собрать сообщение в каноническом формате «ключ: значение»."""
    return {
        "состояние": состояние,
        "координата": tuple(координата),
        "данные": данные or {},
    }


def _as_coord(value: Any) -> Coord:
    x, y, z = value
    return (int(x), int(y), int(z))


# ---------------------------------------------------------------------------
# Блок
# ---------------------------------------------------------------------------
@dataclass
class Block:
    """Один блок мира.

    Атрибуты:
        координата   — положение блока (x, y, z);
        память       — сохранённые данные (любая вложенность);
                       специальный ключ "программа" — список сообщений,
                       исполняемых при получении сигнала;
        соединения   — координаты блоков, с которыми блок соединён;
        заблокирован — блок не пропускает и не принимает сигнал;
        выделен      — входит ли блок в текущее выделение;
        цвет         — цвет состояния (используется и логикой, и UI);
        сигнал       — последние принятые данные передачи (None — нет сигнала).
    """

    координата: Coord
    память: Dict[str, Any] = field(default_factory=dict)
    соединения: Set[Coord] = field(default_factory=set)
    заблокирован: bool = False
    выделен: bool = False
    цвет: str = "#9e9e9e"
    сигнал: Optional[Dict[str, Any]] = None

    # -- данные ------------------------------------------------------------
    def сохранить(self, данные: Dict[str, Any]) -> None:
        """Слить вложенный словарь данных в память блока."""
        _deep_merge(self.память, copy.deepcopy(данные))

    def стереть(self, ключи: Optional[Iterable[str]] = None) -> None:
        """Стереть указанные ключи памяти (или всю память целиком)."""
        if ключи is None:
            self.память.clear()
        else:
            for ключ in ключи:
                self.память.pop(ключ, None)

    # -- сериализация --------------------------------------------------------
    def to_dict(self) -> Dict[str, Any]:
        return {
            "координата": list(self.координата),
            "память": self.память,
            "соединения": [list(c) for c in sorted(self.соединения)],
            "заблокирован": self.заблокирован,
            "выделен": self.выделен,
            "цвет": self.цвет,
            "сигнал": self.сигнал,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "Block":
        return cls(
            координата=_as_coord(data["координата"]),
            память=data.get("память", {}),
            соединения={_as_coord(c) for c in data.get("соединения", [])},
            заблокирован=data.get("заблокирован", False),
            выделен=data.get("выделен", False),
            цвет=data.get("цвет", "#9e9e9e"),
            сигнал=data.get("сигнал"),
        )


def _deep_merge(dst: Dict[str, Any], src: Dict[str, Any]) -> None:
    for key, value in src.items():
        if isinstance(value, dict) and isinstance(dst.get(key), dict):
            _deep_merge(dst[key], value)
        else:
            dst[key] = value


# ---------------------------------------------------------------------------
# Мир
# ---------------------------------------------------------------------------
class World:
    """Разреженный 3D-мир блоков с диспетчером сообщений."""

    def __init__(self) -> None:
        self.блоки: Dict[Coord, Block] = {}
        self.журнал: List[Message] = []
        # Подписчики получают каждое обработанное сообщение (например, UI).
        self.наблюдатели: List[Callable[[Message], None]] = []
        self._обработчики: Dict[str, Callable[[Coord, Dict[str, Any]], None]] = {
            СОСТОЯНИЯ["ГЕНЕРАЦИЯ"]: self._генерация,
            СОСТОЯНИЯ["УНИЧТОЖЕНИЕ"]: self._уничтожение,
            СОСТОЯНИЯ["СОХРАНЕНИЕ"]: self._сохранение,
            СОСТОЯНИЯ["СТИРАНИЕ"]: self._стирание,
            СОСТОЯНИЯ["ПЕРЕДАЧА"]: self._передача,
            СОСТОЯНИЯ["БЛОКИРОВКА"]: self._блокировка,
            СОСТОЯНИЯ["РАЗБЛОКИРОВКА"]: self._разблокировка,
            СОСТОЯНИЯ["ВЫДЕЛЕНИЕ"]: self._выделение,
            СОСТОЯНИЯ["СНЯТИЕ_ВЫДЕЛЕНИЯ"]: self._снятие_выделения,
            СОСТОЯНИЯ["СОЕДИНЕНИЕ"]: self._соединение,
            СОСТОЯНИЯ["РАЗЪЕДИНЕНИЕ"]: self._разъединение,
            СОСТОЯНИЯ["ПЕРЕМЕЩЕНИЕ"]: self._перемещение,
        }

    # -- единая точка входа ---------------------------------------------------
    def dispatch(self, msg: Message) -> None:
        """Обработать сообщение формата {состояние, координата, данные}.

        Через эту функцию проходят и клики мыши, и сообщения из
        «программ», записанных в данных самих блоков.
        """
        состояние = msg.get("состояние")
        координата = _as_coord(msg.get("координата", (0, 0, 0)))
        данные = msg.get("данные") or {}

        обработчик = self._обработчики.get(состояние)
        if обработчик is None:
            raise ValueError(f"Неизвестное состояние: {состояние!r}")

        self.журнал.append(copy.deepcopy(msg))
        обработчик(координата, данные)
        for наблюдатель in self.наблюдатели:
            наблюдатель(msg)

    # -- генерация / уничтожение ---------------------------------------------
    def _генерация(self, координата: Coord, данные: Dict[str, Any]) -> None:
        блок = self.блоки.get(координата)
        if блок is None:
            блок = Block(координата=координата)
            self.блоки[координата] = блок
        if "цвет" in данные:
            блок.цвет = данные["цвет"]
        if "память" in данные:
            блок.сохранить(данные["память"])

    def _уничтожение(self, координата: Coord, данные: Dict[str, Any]) -> None:
        блок = self.блоки.pop(координата, None)
        if блок is None:
            return
        # Убрать обратные ссылки соединений.
        for сосед in блок.соединения:
            if сосед in self.блоки:
                self.блоки[сосед].соединения.discard(координата)

    # -- сохранение / стирание -------------------------------------------------
    def _сохранение(self, координата: Coord, данные: Dict[str, Any]) -> None:
        блок = self._требовать(координата)
        блок.сохранить(данные)

    def _стирание(self, координата: Coord, данные: Dict[str, Any]) -> None:
        блок = self._требовать(координата)
        блок.стереть(данные.get("ключи"))
        if данные.get("сбросить_сигнал"):
            блок.сигнал = None

    # -- передача / блокировка --------------------------------------------------
    def _передача(self, координата: Coord, данные: Dict[str, Any]) -> None:
        """Передать данные от блока по всем соединениям (волной, как редстоун).

        Заблокированные блоки сигнал не принимают и дальше не передают.
        Если в памяти блока есть "программа" (список сообщений) — она
        исполняется при приходе сигнала: так функции передаются через
        данные внутри блоков.
        """
        старт = self._требовать(координата)
        if старт.заблокирован:
            return

        очередь: deque[Coord] = deque([координата])
        посещено: Set[Coord] = {координата}
        while очередь:
            тек = очередь.popleft()
            блок = self.блоки.get(тек)
            if блок is None or блок.заблокирован:
                continue
            блок.сигнал = copy.deepcopy(данные)
            self._исполнить_программу(блок)
            for сосед in блок.соединения:
                if сосед not in посещено:
                    посещено.add(сосед)
                    очередь.append(сосед)

    def _исполнить_программу(self, блок: Block) -> None:
        программа = блок.память.get("программа")
        if not isinstance(программа, list):
            return
        for вложенное in программа:
            if isinstance(вложенное, dict) and "состояние" in вложенное:
                msg = copy.deepcopy(вложенное)
                # Если координата не указана — действие применяется к самому блоку.
                msg.setdefault("координата", блок.координата)
                self.dispatch(msg)

    def _блокировка(self, координата: Coord, данные: Dict[str, Any]) -> None:
        блок = self._требовать(координата)
        блок.заблокирован = True
        блок.сигнал = None

    def _разблокировка(self, координата: Coord, данные: Dict[str, Any]) -> None:
        self._требовать(координата).заблокирован = False

    # -- выделение группы (в т.ч. цветом) ---------------------------------------
    def _выделение(self, координата: Coord, данные: Dict[str, Any]) -> None:
        """Выделить группу блоков и (опционально) покрасить её.

        данные:
            "область": [[x1,y1,z1], [x2,y2,z2]] — параллелепипед выделения
                       (если не задана — выделяется блок в "координата");
            "цвет":    цвет состояния для выделенной группы;
            "связные": true — выделить всю связную (соединённую) группу.
        """
        цели = self._цели_выделения(координата, данные)
        цвет = данные.get("цвет")
        for c in цели:
            блок = self.блоки.get(c)
            if блок is None:
                continue
            блок.выделен = True
            if цвет:
                блок.цвет = цвет

    def _снятие_выделения(self, координата: Coord, данные: Dict[str, Any]) -> None:
        if данные.get("все"):
            for блок in self.блоки.values():
                блок.выделен = False
            return
        for c in self._цели_выделения(координата, данные):
            блок = self.блоки.get(c)
            if блок is not None:
                блок.выделен = False

    def _цели_выделения(self, координата: Coord, данные: Dict[str, Any]) -> List[Coord]:
        if данные.get("связные"):
            return sorted(self.связная_группа(координата))
        область = данные.get("область")
        if область:
            (x1, y1, z1), (x2, y2, z2) = (_as_coord(область[0]), _as_coord(область[1]))
            lo = (min(x1, x2), min(y1, y2), min(z1, z2))
            hi = (max(x1, x2), max(y1, y2), max(z1, z2))
            return [
                c for c in self.блоки
                if all(lo[i] <= c[i] <= hi[i] for i in range(3))
            ]
        return [координата]

    # -- соединение / разъединение ------------------------------------------------
    def _соединение(self, координата: Coord, данные: Dict[str, Any]) -> None:
        a = self._требовать(координата)
        b = self._требовать(_as_coord(данные["с"]))
        a.соединения.add(b.координата)
        b.соединения.add(a.координата)

    def _разъединение(self, координата: Coord, данные: Dict[str, Any]) -> None:
        a = self._требовать(координата)
        if "с" in данные:
            цель = _as_coord(данные["с"])
            a.соединения.discard(цель)
            if цель in self.блоки:
                self.блоки[цель].соединения.discard(координата)
        else:
            # Разорвать все связи блока.
            for сосед in list(a.соединения):
                if сосед in self.блоки:
                    self.блоки[сосед].соединения.discard(координата)
            a.соединения.clear()

    # -- перемещение соединённой группы вместе с данными --------------------------
    def _перемещение(self, координата: Coord, данные: Dict[str, Any]) -> None:
        """Переместить связную группу блоков на вектор "сдвиг".

        Блоки переезжают вместе со своей памятью, сигналом, цветом и
        соединениями (данные соединённых групп перемещаются целиком).
        """
        dx, dy, dz = данные.get("сдвиг", (0, 0, 0))
        if (dx, dy, dz) == (0, 0, 0):
            return
        if данные.get("только_выделенные"):
            группа = {c for c, b in self.блоки.items() if b.выделен}
        else:
            группа = self.связная_группа(координата)
        if not группа:
            return

        новые_места = {c: (c[0] + dx, c[1] + dy, c[2] + dz) for c in группа}
        # Нельзя въехать в чужой блок (не входящий в перемещаемую группу).
        for новое in новые_места.values():
            if новое in self.блоки and новое not in группа:
                raise ValueError(f"Место занято: {новое}")

        перемещаемые = {c: self.блоки.pop(c) for c in группа}
        for старое, блок in перемещаемые.items():
            новое = новые_места[старое]
            блок.координата = новое
            блок.соединения = {
                новые_места.get(сосед, сосед) for сосед in блок.соединения
            }
            self.блоки[новое] = блок
        # Поправить ссылки внешних блоков на переехавшие.
        for блок in self.блоки.values():
            блок.соединения = {новые_места.get(c, c) for c in блок.соединения}

    # -- утилиты ------------------------------------------------------------------
    def связная_группа(self, координата: Coord) -> Set[Coord]:
        """Все блоки, достижимые из данного по соединениям."""
        if координата not in self.блоки:
            return set()
        группа: Set[Coord] = {координата}
        очередь: deque[Coord] = deque([координата])
        while очередь:
            тек = очередь.popleft()
            for сосед in self.блоки[тек].соединения:
                if сосед in self.блоки and сосед not in группа:
                    группа.add(сосед)
                    очередь.append(сосед)
        return группа

    def _требовать(self, координата: Coord) -> Block:
        блок = self.блоки.get(координата)
        if блок is None:
            raise KeyError(f"Блока нет в координате {координата}")
        return блок

    # -- сохранение/загрузка мира в JSON --------------------------------------------
    def save(self, path: str) -> None:
        data = {"блоки": [b.to_dict() for b in self.блоки.values()]}
        with open(path, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

    @classmethod
    def load(cls, path: str) -> "World":
        world = cls()
        with open(path, encoding="utf-8") as f:
            data = json.load(f)
        for raw in data.get("блоки", []):
            блок = Block.from_dict(raw)
            world.блоки[блок.координата] = блок
        return world
