"""Тесты ядра. Запуск: python -m redstone.tests.test_core (или pytest)."""

import json
import os
import tempfile

from redstone.core import World, СОСТОЯНИЯ, сообщение

С = СОСТОЯНИЯ


def test_генерация_и_уничтожение():
    w = World()
    w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (1, 2, 3), {"цвет": "#ff0000"}))
    assert (1, 2, 3) in w.блоки
    assert w.блоки[(1, 2, 3)].цвет == "#ff0000"

    w.dispatch(сообщение(С["УНИЧТОЖЕНИЕ"], (1, 2, 3)))
    assert (1, 2, 3) not in w.блоки


def test_сохранение_и_стирание_вложенных_данных():
    w = World()
    w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (0, 0, 0)))
    w.dispatch(сообщение(С["СОХРАНЕНИЕ"], (0, 0, 0),
                         {"a": {"b": {"c": 1}}, "x": 5}))
    w.dispatch(сообщение(С["СОХРАНЕНИЕ"], (0, 0, 0), {"a": {"b": {"d": 2}}}))
    блок = w.блоки[(0, 0, 0)]
    assert блок.память == {"a": {"b": {"c": 1, "d": 2}}, "x": 5}

    w.dispatch(сообщение(С["СТИРАНИЕ"], (0, 0, 0), {"ключи": ["x"]}))
    assert "x" not in блок.память and "a" in блок.память

    w.dispatch(сообщение(С["СТИРАНИЕ"], (0, 0, 0)))
    assert блок.память == {}


def test_передача_и_блокировка():
    w = World()
    for x in range(4):
        w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (x, 0, 0)))
    for x in range(3):
        w.dispatch(сообщение(С["СОЕДИНЕНИЕ"], (x, 0, 0), {"с": [x + 1, 0, 0]}))

    w.dispatch(сообщение(С["БЛОКИРОВКА"], (2, 0, 0)))
    w.dispatch(сообщение(С["ПЕРЕДАЧА"], (0, 0, 0),
                         {"сигнал": {"уровень": 15, "вглубь": {"метка": "ok"}}}))

    assert w.блоки[(0, 0, 0)].сигнал["сигнал"]["уровень"] == 15
    assert w.блоки[(1, 0, 0)].сигнал is not None
    assert w.блоки[(2, 0, 0)].сигнал is None      # заблокирован
    assert w.блоки[(3, 0, 0)].сигнал is None      # сигнал не прошёл дальше

    w.dispatch(сообщение(С["РАЗБЛОКИРОВКА"], (2, 0, 0)))
    w.dispatch(сообщение(С["ПЕРЕДАЧА"], (0, 0, 0), {"сигнал": {"уровень": 7}}))
    assert w.блоки[(3, 0, 0)].сигнал["сигнал"]["уровень"] == 7


def test_программа_внутри_блока():
    """Функции передаются через данные внутри блоков: при приходе сигнала
    блок исполняет свою «программу» — список таких же сообщений."""
    w = World()
    w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (0, 0, 0)))
    w.dispatch(сообщение(С["СОХРАНЕНИЕ"], (0, 0, 0), {
        "программа": [
            # сгенерировать новый блок и покрасить себя
            {"состояние": С["ГЕНЕРАЦИЯ"], "координата": [5, 5, 0],
             "данные": {"цвет": "#00ff00"}},
            {"состояние": С["ВЫДЕЛЕНИЕ"], "данные": {"цвет": "#ff00ff"}},
        ],
    }))
    w.dispatch(сообщение(С["ПЕРЕДАЧА"], (0, 0, 0), {"пуск": True}))

    assert (5, 5, 0) in w.блоки and w.блоки[(5, 5, 0)].цвет == "#00ff00"
    assert w.блоки[(0, 0, 0)].цвет == "#ff00ff" and w.блоки[(0, 0, 0)].выделен


def test_выделение_области_цветом_и_снятие():
    w = World()
    for x in range(3):
        for y in range(3):
            w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (x, y, 0)))

    w.dispatch(сообщение(С["ВЫДЕЛЕНИЕ"], (0, 0, 0),
                         {"область": [[0, 0, 0], [1, 1, 0]], "цвет": "#ffff00"}))
    assert w.блоки[(1, 1, 0)].выделен and w.блоки[(1, 1, 0)].цвет == "#ffff00"
    assert not w.блоки[(2, 2, 0)].выделен

    w.dispatch(сообщение(С["СНЯТИЕ_ВЫДЕЛЕНИЯ"], (0, 0, 0), {"все": True}))
    assert not any(b.выделен for b in w.блоки.values())


def test_соединение_разъединение_и_связная_группа():
    w = World()
    for x in range(3):
        w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (x, 0, 0)))
    w.dispatch(сообщение(С["СОЕДИНЕНИЕ"], (0, 0, 0), {"с": [1, 0, 0]}))
    w.dispatch(сообщение(С["СОЕДИНЕНИЕ"], (1, 0, 0), {"с": [2, 0, 0]}))
    assert w.связная_группа((0, 0, 0)) == {(0, 0, 0), (1, 0, 0), (2, 0, 0)}

    w.dispatch(сообщение(С["РАЗЪЕДИНЕНИЕ"], (1, 0, 0), {"с": [2, 0, 0]}))
    assert w.связная_группа((0, 0, 0)) == {(0, 0, 0), (1, 0, 0)}


def test_перемещение_группы_с_данными():
    w = World()
    for x in range(2):
        w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (x, 0, 0)))
    w.dispatch(сообщение(С["СОЕДИНЕНИЕ"], (0, 0, 0), {"с": [1, 0, 0]}))
    w.dispatch(сообщение(С["СОХРАНЕНИЕ"], (0, 0, 0), {"груз": {"вес": 10}}))
    # отдельный блок, не входящий в группу
    w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (9, 9, 0)))

    w.dispatch(сообщение(С["ПЕРЕМЕЩЕНИЕ"], (0, 0, 0), {"сдвиг": [3, 2, 1]}))

    assert (3, 2, 1) in w.блоки and (4, 2, 1) in w.блоки
    assert (0, 0, 0) not in w.блоки and (1, 0, 0) not in w.блоки
    # данные переехали вместе с блоком, соединения сохранились
    assert w.блоки[(3, 2, 1)].память == {"груз": {"вес": 10}}
    assert (4, 2, 1) in w.блоки[(3, 2, 1)].соединения
    # посторонний блок не сдвинулся
    assert (9, 9, 0) in w.блоки


def test_сохранение_и_загрузка_мира():
    w = World()
    w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (0, 0, 0), {"цвет": "#123456"}))
    w.dispatch(сообщение(С["ГЕНЕРАЦИЯ"], (1, 0, 0)))
    w.dispatch(сообщение(С["СОЕДИНЕНИЕ"], (0, 0, 0), {"с": [1, 0, 0]}))
    w.dispatch(сообщение(С["СОХРАНЕНИЕ"], (0, 0, 0), {"a": {"b": [1, 2, 3]}}))

    fd, путь = tempfile.mkstemp(suffix=".json")
    os.close(fd)
    try:
        w.save(путь)
        w2 = World.load(путь)
    finally:
        os.unlink(путь)

    assert w2.блоки[(0, 0, 0)].цвет == "#123456"
    assert w2.блоки[(0, 0, 0)].память == {"a": {"b": [1, 2, 3]}}
    assert (1, 0, 0) in w2.блоки[(0, 0, 0)].соединения


def test_формат_сообщения():
    msg = сообщение("передача", (1, 2, 3), {"данные": {"вложенные": {"ещё": 1}}})
    assert set(msg) == {"состояние", "координата", "данные"}
    assert msg["координата"] == (1, 2, 3)
    json.dumps(msg, ensure_ascii=False, default=list)  # сериализуемо


